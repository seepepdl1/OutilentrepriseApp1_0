library(bs4Dash)

# ----------------------------------------------------------------------------------------------------
# > FONCTIONS
# ----------------------------------------------------------------------------------------------------


# ----------------------------------------------------------------------------------------------------
# > CONSTANTES
# ----------------------------------------------------------------------------------------------------


# ----------------------------------------------------------------------------------------------------
# > UI
# ----------------------------------------------------------------------------------------------------

# Header

header <- bs4DashNavbar(title = "Outil entreprise")
  
# Sidebar

sidebar <- bs4DashSidebar("Hello")

# Body

body <- bs4DashBody(

  fluidRow("Hello")
  
  )

# Footer

footer <- bs4DashFooter("Hello")

# Ui

ui <- bs4DashPage(header = header, 
                  sidebar = sidebar, 
                  body = body, 
                  footer = footer, 
                  title="Outil entreprise",
                  dark = NULL,
                  scrollToTop = TRUE)


# ----------------------------------------------------------------------------------------------------
# > SERVER
# ----------------------------------------------------------------------------------------------------

server <- function(input, output) {

  set.seed(122)
  
  histdata <- rnorm(500)
  
  output$plot1 <- renderPlot({data <- histdata[seq_len(input$slider)]
   
                             hist(data)})
}


# ----------------------------------------------------------------------------------------------------
# > APP
# ----------------------------------------------------------------------------------------------------

shinyApp(ui, server)