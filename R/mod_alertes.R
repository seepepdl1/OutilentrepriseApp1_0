fctAlertesHeader <- function(idNotif, 
                             labelNotif, 
                             textNotif,
                             dateNotif, 
                             linkNotif) {
  bs4Dash::notificationItem(
    inputId = idNotif,
    text = HTML(glue("{labelNotif} 
                    <span class='float-right text-muted text-sm'>
                      {dateNotif}
                    </span>")),
    icon = shiny::icon('info'),
    status = 'gray-dark'
  )
}
